
public class MoneyBagDTO {
	private Float money;
	private String inout;
	/**
	 * @return 获得 money
	 */
	public Float getMoney() {
		return money;
	}
	/**
	 * @param money 设置 money
	 */
	public void setMoney(Float money) {
		this.money = money;
	}
	/**
	 * @return 获得 inout
	 */
	public String getInout() {
		return inout;
	}
	/**
	 * @param inout 设置 inout
	 */
	public void setInout(String inout) {
		this.inout = inout;
	}
	
	
}
